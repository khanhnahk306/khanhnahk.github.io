public class Calculator {
    public static int sum(int a, int b){
        return a + b;
    }
    public static int sub(int a, int b){
        return a - b;
    }
    public static int mul(int a, int b){
        return a * b;
    }
    public static float div(int a, int b){
        return a / b;
    }
    public static double pow(int a, int b){
        return Math.pow(a,b);
    }

}
